#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && s1[i] && s1[i] == s2[i])
	{
		i++;
	}
	if (i == n)
	{
		return (0);
	}
	return (s1[i] - s2[i]);
}

int	main(void)
{
	char	str1[5] = "abcd";
	char	str2[5] = "abcde";

	printf("%d\n", ft_strncmp(str1, str2, 5));
	return (0);
}
